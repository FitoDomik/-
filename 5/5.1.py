# Пояснения к формулам и примеры
# 1) bN = bin(N)[2:]
#    Формула: bin(N) -> строка вида '0b...' ; [2:] убирает префикс '0b'.
#    Пример: N = 13 -> bin(13) = '0b1101' -> bN = '1101'.
#
# 2) N % 3 == 0
#    Формула: остаток от деления на 3 равен 0 — число кратно 3.
#    Пример: N = 12 -> 12 % 3 = 0 (кратно); N = 14 -> 14 % 3 = 2 (не кратно).
#
# 3) bN += bN[-3:]
#    Формула: дописать к bN её последние 3 бита (срез последних 3 символов).
#    Пример: bN = '1101' -> bN[-3:] = '101' -> новая bN = '1101' + '101' = '1101101'.
#
# 4) r = bin((N % 3) * 3)[2:]
#    Формула: взять остаток N по модулю 3, умножить на 3, перевести в двоичную запись без '0b'.
#    Примеры:
#      N = 14 -> N % 3 = 2; (2*3)=6; bin(6)='0b110' -> r='110'.
#      N = 11 -> N % 3 = 2; r='110'.
#      N = 10 -> N % 3 = 1; (1*3)=3; bin(3)='0b11' -> r='11'.
#
# 5) bN += r
#    Формула: конкатенация строк: к исходной двоичной записи дописываем r.
#    Пример: bN='1010', r='11' -> bN='101011'.
#
# 6) int(bN, 2)
#    Формула: интерпретация строки bN как двоичного числа и перевод в десятичное.
#    Примеры: int('1101',2)=13; int('101011',2)=43.
#
# 7) for N in range(1, 300):
#    Формула: перебираем N = 1..299 включительно.
#
# 8) if R > 151: print(R)
#    Формула: фильтруем результаты по условию R > 151, выводим подходящие значения R.
#    При необходимости можно печатать и N: print(N, R).
